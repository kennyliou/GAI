
public class LAB {
	private double L;
	private double a;
	private double b;
	public LAB() {
		L = 0;
		a = 0;
		b = 0;
	}
	public LAB(double L, double a, double b) {
		this.L = L;
		this.a = a;
		this.b = b;
	}
	public double getL() {
		return this.L;
	}
	public double geta() {
		return this.a;
	}
	public double getb() {
		return this.b;
	}
}
